    private void btncalificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalificarActionPerformed
        // TODO add your handling code here:
        btnjugar.setEnabled(false);
        
        String palabra=(String) palabras.toArray()[x-1];
        if(palabra.equalsIgnoreCase(tfrespuesta.getText())){
            puntos=puntos+10;
            JOptionPane.showMessageDialog(null,"Respuesta Correcta");
            String pal=(String) palabras.toArray()[x];
            ImageIcon foto;
             foto=new ImageIcon(getClass().getResource("/imagenes/"+pal+".jpg"));
            lbimagen.setIcon(foto);
            String palmix="";
            int tam=pal.length();
            ArrayList<Integer> nums = new ArrayList<>(tam);
            for(int i=0;i<tam;i++){
                 nums.add(-1);
            }
            char let;
            String aux;
            int indice;
            for(int i=0;i<tam;){
                 indice = (int) Math.floor(Math.random()*((tam-1)-0+1)+0);
                if(numrep(indice,tam,nums)==0){
                     let=pal.charAt(indice);
                    aux=Character.toString(let);
                    palmix=palmix.concat(aux);
                    nums.set(i++,indice);
                }
            }
            lbpalabra.setText(palmix);
            x++;
         }else{
                JOptionPane.showMessageDialog(null,"Respuesta Incorrecta,intentalo de nuevo");
                vidas--;
                if(vidas==0){
                    JOptionPane.showMessageDialog(null,"Juegos Terminado");
                    MenuPrincipal n=new MenuPrincipal();
                     n.setVisible(true);
                    this.setVisible(false);
                }
        }
        lbpuntos.setText("Puntos: "+puntos);
        String vid="Vidas: ";
         
        for (int i = 0; i < vidas; i++) {
            vid=vid.concat("*");
        }
        lbvidas.setText(vid);
    }//GEN-LAST:event_btncalificarActionPerformed
    static int numrep(int num,int tam,ArrayList nums){
        int i=0,repe=0;
        int aux=0;
        for(i=0;i<tam;i++){
            aux=(int) nums.toArray()[i];
            if(aux==num){
                repe=1;
            }
        }
        return repe;
    }
