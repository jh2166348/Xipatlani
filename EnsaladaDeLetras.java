    private void btnjugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnjugarActionPerformed
        // TODO add your handling code here:
        btncalificar.setEnabled(true);
        palabras.add("cihuatl");//esposa o mujer
        palabras.add("colli");//anciano o abuelo
         palabras.add("piltzintli");//bebe
         palabras.add("oquichtli");//hombre
         Graphics papel=panel.getGraphics();
         papel.setColor(Color.WHITE);
         
         //papel.setColor(Color.CYAN);
         String pal=(String) palabras.toArray()[0];
         ImageIcon foto;
         foto=new ImageIcon(getClass().getResource("/imagenes/"+pal+".jpg"));
         lbimagen.setIcon(foto);
         String palmix="";
         int tam=pal.length();
         ArrayList<Integer> nums = new ArrayList<>(tam);
         for(int i=0;i<tam;i++){
             nums.add(-1);
         }
         char let;
         String aux;
           int indice;
         for(int i=0;i<tam;){
             indice = (int) Math.floor(Math.random()*((tam-1)-0+1)+0);
             if(numrep(indice,tam,nums)==0){
                 let=pal.charAt(indice);
                 aux=Character.toString(let);
                 palmix=palmix.concat(aux);
                 nums.set(i++,indice);
             }
         }
         lbpalabra.setText(palmix);
         lbpuntos.setText("Puntos: "+puntos);
        String vid="Vidas: ";
         
        for (int i = 0; i < vidas; i++) {
            vid=vid.concat("*");
        }
        lbvidas.setText(vid);
    }//GEN-LAST:event_btnjugarActionPerformed
